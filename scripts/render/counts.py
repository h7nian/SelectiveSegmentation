def parse_args(argv=None):
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument(
        "--mode",
        choices=("count", "partition", "copula"),
        default="count",
    )
    parser.add_argument("--analysis", required=True)
    parser.add_argument("--output-dir", required=True)
    return parser.parse_args(argv)


def _load_count(path: Path) -> dict:
    value = json.loads(path.read_text(encoding="utf-8"))
    if value.get("analysis_id") != "dice-count-result-analysis-v1":
        raise ValueError("unexpected Dice count analysis")
    conditions = value.get("conditions")
    if not isinstance(conditions, list) or len(conditions) != 10:
        raise ValueError("Dice count rendering requires ten conditions")
    observed = {(row["dataset"], row["condition"]) for row in conditions}
    expected = {(dataset, model) for dataset, _ in DATASETS for model, _ in MODELS}
    if observed != expected:
        raise ValueError("Dice count analysis has an unexpected condition set")
    return value


def _load_partition(path: Path) -> dict:
    value = json.loads(path.read_text(encoding="utf-8"))
