def _forest(analysis: dict, output: Path) -> None:
    import matplotlib.pyplot as plt
    import numpy as np

    conditions = sorted(
        analysis["conditions"],
        key=lambda row: (
            [dataset for dataset, _ in DATASETS].index(row["dataset"]),
            [model for model, _ in MODELS].index(row["condition"]),
        ),
    )
    differences = np.asarray(
        [row["primary_comparison"]["difference_left_minus_right"] for row in conditions]
    ) * 100
    low = np.asarray(
        [row["primary_comparison"]["bootstrap"]["ci_low"] for row in conditions]
    ) * 100
    high = np.asarray(
        [row["primary_comparison"]["bootstrap"]["ci_high"] for row in conditions]
    ) * 100
    labels = [
        f"{dict(DATASETS)[row['dataset']]} / "
        + ("CLIP-T" if row["condition"] == "clipseg-target" else "DL-T")
        for row in conditions
    ]
    y = np.arange(len(conditions))[::-1]
    figure, axis = plt.subplots(figsize=(6.8, 4.4))
    axis.axvline(0, color="0.45", linewidth=1, linestyle="--")
    colors = np.where(differences < 0, "#004c99", "#a33a2b")
    for position, center, lower, upper, color in zip(
        y, differences, low, high, colors, strict=True
    ):
        axis.errorbar(
            center,
            position,
            xerr=[[center - lower], [upper - center]],
            fmt="o",
            color=color,
            capsize=2,
            markersize=4,
            linewidth=1.1,
        )
    axis.set_yticks(y, labels)
    axis.set_xlabel("Dice AURC difference ×100 (two-block − LevelSet-Q)")
    axis.set_title("Negative values favor the Dice count posterior")
    axis.grid(axis="x", color="0.9", linewidth=0.7)
    figure.tight_layout()
    figure.savefig(output, bbox_inches="tight", metadata={"Creator": __name__})
    plt.close(figure)


