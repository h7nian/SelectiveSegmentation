def main(argv=None):
    args = parse_args(argv)
    analysis_path = Path(args.analysis)
    output = Path(args.output_dir)
    output.mkdir(parents=True, exist_ok=True)
    if args.mode == "copula":
        analysis = _load_copula(analysis_path)
        table_path = output / "dice_spatial_copula.tex"
        figure_path = output / "dice_spatial_copula_forest.pdf"
        manifest_path = output / "dice_spatial_copula_render.manifest.json"
        if any(
            path.exists() or path.is_symlink()
            for path in (table_path, figure_path, manifest_path)
        ):
            raise FileExistsError("spatial-copula render output already exists")
        analysis_sha256 = sha256_file(analysis_path)
        table_path.write_text(
            _copula_table(analysis, analysis_sha256), encoding="utf-8"
        )
        _copula_forest(analysis, figure_path)
        manifest = {
            "schema_version": 1,
            "analysis_sha256": analysis_sha256,
            "table_sha256": sha256_file(table_path),
            "figure_sha256": sha256_file(figure_path),
            "source_sha256": hashlib.sha256(Path(__file__).read_bytes()).hexdigest(),
        }
        manifest_path.write_text(
            json.dumps(manifest, indent=2) + "\n", encoding="utf-8"
        )
        print(f"saved {table_path}")
        print(f"saved {figure_path}")
        return
    if args.mode == "partition":
        analysis = _load_partition(analysis_path)
        table_path = output / "dice_partition_ladder.tex"
        manifest_path = output / "dice_partition_ladder_render.manifest.json"
        if any(path.exists() or path.is_symlink() for path in (table_path, manifest_path)):
            raise FileExistsError("Dice partition render output already exists")
        analysis_sha256 = sha256_file(analysis_path)
        table_path.write_text(
            _partition_table(analysis, analysis_sha256), encoding="utf-8"
        )
        manifest = {
            "schema_version": 1,
            "analysis_sha256": analysis_sha256,
            "table_sha256": sha256_file(table_path),
            "source_sha256": hashlib.sha256(Path(__file__).read_bytes()).hexdigest(),
        }
        manifest_path.write_text(json.dumps(manifest, indent=2) + "\n", encoding="utf-8")
        print(f"saved {table_path}")
        return

    analysis = _load_count(analysis_path)
    table_path = output / "dice_count_posterior.tex"
    figure_path = output / "dice_count_posterior_forest.pdf"
    manifest_path = output / "dice_count_posterior_render.manifest.json"
    if any(path.exists() or path.is_symlink() for path in (table_path, figure_path, manifest_path)):
        raise FileExistsError("Dice count render output already exists")
    analysis_sha256 = sha256_file(analysis_path)
    table_path.write_text(_table(analysis, analysis_sha256), encoding="utf-8")
    _forest(analysis, figure_path)
    manifest = {
        "schema_version": 1,
        "analysis_sha256": analysis_sha256,
        "table_sha256": sha256_file(table_path),
        "figure_sha256": sha256_file(figure_path),
        "source_sha256": hashlib.sha256(Path(__file__).read_bytes()).hexdigest(),
    }
    manifest_path.write_text(json.dumps(manifest, indent=2) + "\n", encoding="utf-8")
    print(f"saved {table_path}")
    print(f"saved {figure_path}")


if __name__ == "__main__":
    main()
